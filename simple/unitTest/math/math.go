package math

import "math"

// Max
// @Description: Max returns the largest of x or y.
// @param x
// @param y
// @return float64
func Max(x, y float64) float64 {
	return math.Max(x, y)
}

// Abs
// @Description: Abs returns the absolute value of x.
// @param x
// @return float64
func Abs(x float64) float64 {
	return math.Abs(x)
}
